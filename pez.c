/*
   ( pid flags -- status )
   Waits for the pid to exit, and returns its status.
*/
prim P_waitpid(pez_instance *p)
{
	Sl(2);
	int pid, status, options;

	pid = (int)S1;
	options = (int)S0;
	Pop;

	waitpid(pid, &status, options);
	S0 = (pez_stackitem)status;
}

