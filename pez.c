// Just a few macros for the somewhat complicated FFI stuff:
#define emit(s) (ccode = pez_strcat(ccode, (s)))
#define RETFLOAT 1
#define RETINT 2

/*
   Adds the includes from lib to the C code, returns the result as a GC'd
   string.
*/
static char *add_ffi_includes(pez_ffi_lib *lib, char *ccode)
{
	pez_ffi_include *inc;

	inc = lib->includes;
	while(inc) {
		emit("#include <");
		emit(inc->fname);
		emit(">\n");
		inc = inc->next;
	}

	return ccode;
}

#define tbsz 1024
/*
   Define an FFI call.
*/
prim P_ffi_colon(pez_instance *p)
{
	// Only ~17 local vars and only ~200 lines!  Brevity, clean design, this
	// function has it al--oh, who am I kidding, FIXME all over this chunk.
	TCCState *s;
	long fsz;
	int i, arglen, nstackitems = 0, rs, nfstackitems = 0, fs, retstack = 0;
	unsigned char *args, *ret;
	char *fname, *ccode, tmpbuf[tbsz];
	pez_ffi_lib *lib;
	void *f;

	Sl(4);
	ret = (unsigned char *)S3;
	Hpc(S3);
	fname = (char *)S2;
	Hpc(S2);
	args = (unsigned char *)S1;
	Hpc(S1);
	lib = (pez_ffi_lib *)S0;
	Hpc(S0);
	Npop(4);

	if(ret && !*ret) ret = 0;
	if(args && !*args) args = 0;

	f = dlsym(lib->sofile, fname);

	if(!f) {
		pez_error(p, pez_strcat("Function \"",
			pez_strcat(fname,
				"\" not found when compiling ffi: call!")));
			
		return;
	}

	P_create(p);

	ccode = add_ffi_includes(lib,
			"#include <pez.h>\n"
			"#include <pezdef.h>\n");

	emit("\n"
	     "void ffi_call(pez_instance *p)\n"
	     "{\n");

	// Figure out the stack that the return value goes onto:
	if(ret) {
		switch(*ret) {
		// Return values pushed onto the regular stack:
		case 'p': case 'i': case 'c': case 'l': case 's':
			retstack = RETINT;
			emit("pez_stackitem ret;\n\n");
			break;
		// Return values pushed onto the float stack:
		case 'f': case 'd':
			retstack = RETFLOAT;
			emit("pez_real ret;\n\n");
			break;
		default:
			snprintf(tmpbuf, tbsz,
				"Unrecognized return type \"%c\" in ffi: call "
				"for \"%s\"!", *ret, fname);
			pez_error(p, tmpbuf);
			return;
		}
	}

	// Check to see how many arguments we have, and which stack they come
	// from:
	if(args) {
		arglen = strlen((char *)args);
		for(i = 0; i < arglen; i++) {
			switch(args[i]) {
			// Args that come off the regular stack:
			case 'p': case 'i': case 'c': case 'l': case 's':
				nstackitems++;
				break;
			// Args that come off the float stack:
			case 'f': case 'd':
				nfstackitems++;
				break;
			default:
				snprintf(tmpbuf, tbsz,
					"Unrecognized arg type \"%c\" in ffi: "
					"call for \"%s\"!", args[i], fname);
				pez_error(p, tmpbuf);
				return;
			}
		}

		if(nstackitems > 0) {
			snprintf(tmpbuf, tbsz, "Sl(%d);\nNpop(%d);\n",
					nstackitems, nstackitems);
			emit(tmpbuf);
		}
		if(nfstackitems > 0) {
			snprintf(tmpbuf, tbsz, "FSl(%d);\nNRealpop(%d);\n",
					nfstackitems, nfstackitems);
			emit(tmpbuf);
		}
	}

	// If nothing was popped, we need to check overflow before pushing:
	if(retstack == RETINT) {
		if(!nstackitems)
			emit("So(1);\n");
		emit("ret = (pez_stackitem)");
	} else if(retstack == RETFLOAT) {
		if(!nfstackitems)
			emit("FSo(1);\n");
	}

	if(ret) {
		emit("ret = ");
	}

	emit(fname);
	emit("(");
	if(args && *args) {
		rs = fs = 0;
		for(i = 0; i < arglen; i++) {
			switch(args[i]) {
			// Args that come off the regular stack:
			case 'p': case 'i': case 'c': case 'l': case 's':
				snprintf(tmpbuf, tbsz, "%s(%s)(p->stk[%d])",
					i ? ", " : "",
					ffi_types_table[args[i]], rs++);
				emit(tmpbuf);
				break;
			// Args that come off the float stack:
			case 'f': case 'd':
				snprintf(tmpbuf, tbsz, "%s(%s)p->fstk[%d]",
					i ? ", " : "",
					ffi_types_table[args[i]], fs++);
				emit(tmpbuf);
				break;
			// We've already checked above that the types are sane.
			}
		}
	}
	emit(");\n");

	if(retstack == RETINT) {
		emit("Push = ret;\n");
	} else if(retstack == RETFLOAT && !nfstackitems) {
		emit("Realpush(ret);\n");
	}

	emit("\nreturn;\n}\n");

	// Compile it, finally, damn!
	s = tcc_new();
	if(!s) {
		pez_error(p, "Could not create a new TCC state when "
			"compiling FFI call!");
		return;
	}

	if(tcc_set_output_type(s, TCC_OUTPUT_MEMORY) == -1) {
		pez_error(p, "Couldn't set TCC output type to \"memory\".");
		return;
	}

	if(tcc_compile_string(s, ccode) == -1) {
		pez_error(p, pez_strcat("Couldn't compile FFI call to ",
					fname));
		return;
	}

	tcc_add_symbol(s, fname, f);
	tcc_add_symbol(s, "trouble", trouble);

	fsz = tcc_relocate(s, 0);
	if(fsz < 0) {
		pez_error(p, "Couldn't relocate ffi function!");
		return;
	}

	f = (void *)alloc(fsz);
	tcc_relocate(s, f);
	p->createword->wcode = tcc_get_symbol(s, "ffi_call");
	if(!p->createword->wcode) {
		p->createword->wcode = P_var;
		pez_error(p, "Error getting symbol from compiled FFI function!");
		return;
	}

	tcc_delete(s);
}
